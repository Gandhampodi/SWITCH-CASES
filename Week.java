package SwitchCase;

public class Week {
    public static void main(String[] args) {
     String week="Sunday";
     switch (week) {
         case "Sunday":
             System.out.println("Sunday");
             break;
         case "Monday":
             System.out.println("Monday");
             break;
         case "Tuesday":
             System.out.println("Tuesday");
             break;
         case "Wednesday":
             System.out.println("Wednesday");
             break;
         case "Thrusaday":
             System.out.println("Thrusday");
             break;
         case "Friday":
             System.out.println("Friday");
             break;
         case "saturaday":
             System.out.println("saturday");
             break;
         default:
             System.out.println("march 5 is my brithady");
     }
    }
}
