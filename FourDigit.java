package SwitchCase;

public class FourDigit {
    public static void main(String[] args) {
        int num=786;
        switch(num){
            case 786:
            System.out.println(" number is SEVEN  EIGHT SIX ");
            break;
            case 456:
            System.out.println(" number is four five six");
            break;
            case 746:
            System.out.println("number is seven four six");
            break;
            case 546:
            System.out.println(" number is five four six");
            break;
        default:
            System.out.println("Invalid number");

        }

        }
    }

